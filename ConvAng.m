function OUT = ConvAng(IN)
%---------------------------------------------------------------------
%---------------------------------------------------------------------
%
% This function converts angle on -pi:pi to 0:2*pi
%
%---------------------------------------------------------------------
%
% Dependencies; none. 
%
% Ryans ROMS Matlab and netcdf Utilities 17/7/13
%
%---------------------------------------------------------------------
%---------------------------------------------------------------------
OUT = zeros(size(IN));
OUT(IN<0) = IN(IN<0)+2*pi;
OUT(IN>0) = IN(IN>0);
end
